package com.riskengine.data;

/**
 * Профиль клиента банка.
 *
 * Зачем нужен: чтобы генерировать транзакции, характерные для конкретного типа пользователя.
 *
 * @param id - уникальный ID пользователя (UUID)
 * @param region - географический регион (для проверки "невозможного перемещения")
 * @param ageTier - возрастная группа (влияет на паттерны трат)
 * @param riskSegment - уровень риска: LOW, MEDIUM, HIGH (для реалистичности)
 */
public record UserProfile(
        String id,
        String region,
        AgeTier ageTier,
        RiskSegment riskSegment
) {
    /**
     * Возрастные группы
     * В продакшене использовались бы точные даты рождения.
     */
    public enum AgeTier {
        YOUNG,    // 18-25: активные онлайн-покупки, мелкие суммы
        ADULT,    // 26-50: смешанные траты, крупные покупки
        SENIOR    // 50+: реже, но предсказуемее
    }

    /**
     * Сегмент риска — используется для начальной "разметки" данных.
     * Не является финальным решением, только для генерации.
     */
    public enum RiskSegment {
        LOW, MEDIUM, HIGH
    }
}